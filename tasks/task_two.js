const num1 = 12
const num2 = 3

let answer = num1 + num2 // Add both numbers
console.log(`${num1} + ${num2} = ${answer}`) // @logs 12 + 3 = 15

answer = num1 - num2 // Subtracts both numbers
console.log(`${num1} - ${num2} = ${answer}`) // @logs 12 - 3 = 9

answer = num1 * num2 // mutiplies both numbers
console.log(`${num1} x ${num2} = ${answer}`) // @logs 12 x 3 = 36

answer = num1 / num2 // divides both numbers
console.log(`${num1} รท ${num2} = ${answer}`) // @logs 12 รท 3 = 4

answer = num1 % num2 // gives the remainder from the division of both numbers
console.log(`${num1} % ${num2} = ${answer}`) // @logs 12 % 3 = 0
