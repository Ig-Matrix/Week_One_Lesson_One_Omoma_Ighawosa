let answer = 'This is a normal message'
console.log(answer) // @logs 'This is a normal message' to the console

answer = 'This is a warning message!'
console.warn(answer) // @logs 'This is a warning message!' warning message to the console

answer = 'An error occured!'
console.error(answer) // @logs 'An error occured!' error message to the console
